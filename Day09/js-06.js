// Các toán tử trong JavaScript

// Toán tử số học
let a = 10;
let b = 5;
console.log(a,'+', b, '=', a + b); // 15
console.log(a, '-', b, '=', a - b); // 5
console.log(a, '*', b, '=', a * b); // 50
console.log(a, '/', b, '=', a / b); // 2
console.log(a, '%', b, '=', a % b); // 0    
console.log(a, '**', b, '=', a   ** b); // 100000

// Toán tử gán
let c = 20;
c += 5; // c = c + 5
console.log('c +=5 => c=', c); // 25
c -= 10; // c = c - 10
console.log('c -=10 => c=', c); // 15
c *= 2; // c = c * 2
console.log('c *=2 => c =', c); // 30
c /= 3; // c = c / 3
console.log('c /=3 => c=', c); // 10
c %= 4; // c = c % 4
console.log('c %=4 => c=', c); // 2
c **= 3; // c = c ** 3
console.log('c **=3 => c=', c); // 8 
