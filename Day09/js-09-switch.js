// Cấu trúc switch trong Javascript
let day = 3;
switch (day) {
    case 1:
        console.log('Chủ nhật');
        break;
    case 2:
        console.log('Thứ hai');
        break;
    case 3:
        console.log('Thứ ba');
        break;
    case 4:
        console.log('Thứ tư');
        break;
    case 5:
        console.log('Thứ năm');
        break;
    case 6:
        console.log('Thứ sáu');
        break;
    case 7:
        console.log('Thứ bảy');
        break;
    default:
        console.log('Không phải ngày trong tuần');
}   

